<dialog id="custom_tile_display_help_dialog" style="display: none;">

    <h3>Hidden</h3>
    <p>
        The tile will never be displayed.
    </p>

    <h3>Visible</h3>
    <p>
        The tile will always be displayed.
    </p>


    <h3>Custom</h3>
    <p>
        Tile display will be dynamic and governed by selected field conditions.
    </p>
    <p>Example: Only show the tile when the Contact Status is Active</p>

    <p>In order to create a condition, select a field option and value and click Add to add it to the conditions table.</p>

    <h4>Multiple Conditions</h4>
    <p>Multiple conditions can be specified.</p>
    <p>If <i>Require all selected condition to be true</i> is selected, then <strong>all</strong> conditions must contain valid
    values in order for custom tile to be shown. However, if this option is unchecked, then the custom tile will
    be displayed if <strong>any</strong> condition is satisfied.
    </p>

</dialog>
